    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        Company newCompany = new Company((String) bodyTable.getValueAt(bodyTable.getSelectedRow(), 0));
        String cacString = newCompany.getCheckString(cacNR, cac, cac2, cac7);
        String taxString = newCompany.getCheckString(taxNR, tax);
        String pencomString = newCompany.getCheckString(pencomNR, pencom);
        String itfString = newCompany.getCheckString(itfNR, itf);
        String nsitfString = newCompany.getCheckString(nsitfNR, nsitf);
        String bppString = newCompany.getCheckString(bppNR, bpp);
        String affiString = newCompany.getCheckString(affNR, affi);
        String audString = newCompany.getCheckString(audNR, aud1, aud2, aud3);
        String finString = newCompany.getCheckString(finNR, fin);
        String cvString = newCompany.getCheckString(cvNR, cv, prof);
        String eviString = newCompany.getCheckString(eviNR, evi1, evi2, evi3);
        String trainString = newCompany.getCheckString(trainNR, train);
        String plantString = newCompany.getCheckString(plantNR, plant);
        String ventString = newCompany.getCheckString(ventNR, vent);
        String fwdString = newCompany.getCheckString(fwdNR, fwd);
        float remark = newCompany.getRemark();
        
         int row = bodyTable.getSelectedRow();
        
        bodyTable.setValueAt(cacString, row, 1);
        bodyTable.setValueAt(taxString, row, 2);
        bodyTable.setValueAt(pencomString, row, 3);
        bodyTable.setValueAt(itfString, row, 4);
        bodyTable.setValueAt(nsitfString, row, 5);
        bodyTable.setValueAt(bppString, row, 6);
        bodyTable.setValueAt(affiString, row, 7);
        bodyTable.setValueAt(audString, row, 8);
        bodyTable.setValueAt(finString, row, 9);
        bodyTable.setValueAt(cvString, row, 10);
        bodyTable.setValueAt(eviString, row, 11);
        bodyTable.setValueAt(trainString, row, 12);
        bodyTable.setValueAt(plantString, row, 13);
        bodyTable.setValueAt(ventString, row, 14);
        bodyTable.setValueAt(fwdString, row, 15);
        bodyTable.setValueAt(remark, row, 16);
        

        
        // This blocks of code update the company's data in the database
        try {
          

            Class.forName("org.apache.derby.jdbc.EmbeddedDriver");
            Connection con = DriverManager.getConnection("jdbc:derby:bcdaDB");

            String query2 = "UPDATE  APP." + lotList.getSelectedItem() + " SET CAC = '" + cacString + "', "
                    + "TAX_CERT = '" + taxString + "', "
                    + "PENCOM = '" + pencomString + "', "
                    + "ITF = '" + itfString + "', "
                    + "NSITF = '" + nsitfString + "', "
                    + "BPP_IPR = '" + bppString + "', "
                    + "AFFIDAVIT = '" + affiString + "', "
                    + "AUDIT_ACCOUNT_YEARS = '" + audString + "', "
                    + "F_CAPACITY = '" + finString + "', "
                    + "CV_KEY_STAFF = '" + cvString + "', "
                    + "JOB_EVIDENCES = '" + eviString + "', "
                    + "CMD_CERT = '" + trainString + "', "
                    + "LIST_PLANTS = '" + plantString + "', "
                    + "JOINT_VENTURE = '" + ventString + "', "
                    + "FWD_LETTER = '" + fwdString + "', "
                    + "REMARK = " + remark
                    + " WHERE NAME_OF_COMPANY = '" + newCompany.toString() + "'";
            Statement stmt2 = con.createStatement();
            stmt2.executeUpdate(query2);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(EditCompany.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException e) {
            System.err.println(e);
        }
       
        this.setVisible(false);

    }//GEN-LAST:event_updateActionPerformed

  
