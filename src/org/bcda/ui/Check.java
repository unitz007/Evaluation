    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        try {
            Class.forName("org.apache.derby.jdbc.EmbeddedDriver");
            Connection con = DriverManager.getConnection("jdbc:derby:bcdaDB");
            
            DefaultTableModel model = (DefaultTableModel) bodyTable.getModel();
            String removeRow = (String) model.getValueAt(bodyTable.getSelectedRow(), 0);
            String query2 = "DELETE FROM APP." + lotList.getSelectedItem() + " WHERE NAME_OF_COMPANY = '" + removeRow + "'";
            Statement stmt = con.createStatement();
            stmt.executeUpdate(query2);
            
            model.removeRow(bodyTable.getSelectedRow());
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Check.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Check.class.getName()).log(Level.SEVERE, null, ex);
        }
        doClose(RET_CANCEL);

    }//GEN-LAST:event_okButtonActionPerformed

